public abstract class Team {


    protected Staff s = null;
    protected Staff e = null;
    protected Auto a = null;
    protected String teamname;



    
   

  
    public Team(Staff st, Staff se, Auto au, String tn) {
        s = st;
        e = se;
        a = au;
        teamname = tn;
    }

    public String getTeamname(){
        return teamname;
}
    

 



    


    

   

    

     







}
